#include <stdio.h>

int main() {
    // Declaração das variáveis inteiras
    int A, B, X;

    // Leitura dos dois valores inteiros
    scanf("%d", &A);
    scanf("%d", &B);

    // Soma de A e B
    X = A + B;

    // Impressão do resultado no formato pedido
    printf("X = %d\n", X);

    return 0;
}
